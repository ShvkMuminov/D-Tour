                    <p>
                    <h1>Uzbekistan and Kyrgyzstan Tour</h1> <br>
                    Day 1 <br><br>
                    Arrive in Tashkent. You will be met by D tour driver and transferred to the hotel. Check in to
                    the hotel at 14:00. Meet your guide to start sightseeing in the capital of Uzbekistan, the
                    biggest city in Central Asia.
                    Visit Old city part of Tashkent with the Khast Imam complex where the world famous Quran
                    of Caliph Uthman – Ottoman has been preserved. In Khast-Imam visit Madrassah of Barak
                    khan, Tilla Sheikh mosque, Mausoleum of Abu bakr Kaffal Shashi. Explore Chorsu Bazar.
                    Then take a ride on Tashkent Metro to the city center to visit Applied arts museum and walk
                    through Navoi opera and Ballet theatre to Amir Temur square where you will end Tashkent
                    city tour. Overnight at the hotel. <br>
                    Duration of sightseeing tour: 5-6 hours <br><br>
                    Day 2 <br><br>
                    Early morning transfer for flight to Urgench (1093 km, 1 h. 40 min.). Arrive in Urgench,
                    transfer to the hotel in Khiva (30 min.). Later proceed for guided walking sightseeing tour.
                    Spend a day in Ichan Kala Fortress, a UNESCO World Heritage. Visit Mukhammad
                    Aminkhan, Mukhammad Rakhimkhan Madrassahs, Kalta Minor, Juma mosque, Tash Khovli
                    Palace, Pakhlavan Makhmud Mausoleum, Minaret of Islam Khoja. Free time in the
                    afternoon. Overnight at the hotel. <br>
                    Duration of sightseeing tour: 5 hours <br><br>
                    Day 3 <br><br>
                    In the morning depart Khiva and drive to Bukhara (480 km, 7-8 h.)*. The Great silk
                    road once routed through Bukhara and Khiva ran through the Kyzylkum Desert, as the
                    present road** does nowadays. Stop en-route to enjoy views of Amu Darya River and the
                    desert. Arrive in Bukhara late in the afternoon, check in to the hotel. Time for rest and
                    overnight at the hotel.
                    *As the cafés and restaurants on the way are basic and offer limited menus, you may wish
                    to take a packed lunch on this trip (your hotel or guide can help you arrange this the day
                    before). <br>
                    **Some parts of the road are not in the best condition. <br><br>
                    Day 4 <br><br>
                    Enjoy a walking sightseeing tour with a guide in Old City. Visit Lyabi Khauz ensemble,
                    Magoki Attor mosque, Chor-Minor madrasah, Trading domes, Kalyan minaret, Kalyan
                    Mosque and Mir Arab madrassah, Ulugbek and Abdulazizkhan madrasahs, Ark fortress, Bolo
                    Khauz mosque, Ismail Samani and Chashmai Ayub mausoleums. Overnight at the hotel. <br>
                    Duration of sightseeing tour: 6-7 hours <br><br>
                    Day 5 <br><br>
                    Continue sightseeing tour with a guide. Drive out of the city to Sitorai Mokhi Khosa – the
                    summer residence of Bukhara’s last emir. Head to the Mausoleum of Bakhouddin
                    Naqshbandi. In the afternoon depart to Samarkand (280 km, 4 hours’ drive). Stop on the
                    way at Caravanserai Rabati Malik dating back to 11th century. Arrive in Samarkand late in
                    the afternoon, check in to your hotel. Overnight at the hotel. <br>
                    Duration of sightseeing tour: 2-3 hours <br><br>
                    Day 6 <br><br>
                    Meet your guide and driver at hotel lobby to start city tour. Visit spectacular Registan
                    square, remains of Bibi-Khanum mosque, Siyob Bazaar, Ulugbek Observatory Shakhi zinda
                    Necropolis complex, and Gur Emir Mausoleum – the Mausoleum of Amir Timur. Overnight
                    at the hotel. <br>
                    Duration of sightseeing tour: 6-7 hours <br><br>
                    Day 7 <br><br>
                    Check-out from the hotel till noon. Continue sightseeing: visit Afrasiyab museum and the
                    Tomb of Saint Daniel. Then drive to Konigil village to explore traditional way of making
                    Samarkand paper from mulberry. Enjoy free time with car and driver at your disposal after
                    sightseeing tour. At 16:00 transfer to railway station. Take the high-speed train, Afrosiyob,
                    departing to Tashkent at 17:00 (300 km). Scheduled arrival time is 19:10. Pick-up and
                    transfer to hotel. Overnight at the hotel. <br>
                    Duration of sightseeing tour: 3-4 hours <br><br>
                    Day 8 <br><br>
                    Drive to Ferghana (320 km drive till Fergana city). The steep road* which connects Fergana
                    Valley with the rest of Uzbekistan runs via picturesque mountain pass Kamchik (2267 m.
                    above the sea level). The first stop will be in the city of Kokand where you will visit the
                    Khudoyarkhan palace, Jami mosque, and Norbuta bey Madrassah. Continue driving to
                    Ferghana city (80 km). Visit Akhmad Al-Fergani Park in Fergana. Check in to the hotel.
                    Overnight at the hotel. <br>
                    *Part of the road is currently under reconstruction
                    Duration of sightseeing tour: 4-5 hours <br><br>
                    Day 9 <br><br>
                    In the morning, drive from Fergana to Margilan and visit Said Akhmad Khoja Madrasah.
                    Today, madrasah is home to a number of craftsmen producing handmade carpets and silk.
                    Have a walk on traditional market and continue to Yodgorlik Silk factory to see the
                    production process of silk with traditional Ikat patterns. If it is Thursday or Sunday, visit
                    Kumtepa Bazaar, the largest market in Fergana Valley. Drive to Rishtan to the house of a
                    well-known ceramic master Rustam Usmanov whose works are exhibited on world
                    exhibitions. Enjoy a pottery workshop and have a look at ceramic collection in a small
                    museum. After return to Fergana. Transfer to the hotel. <br>
                    Duration of sightseeing tour: 4-5 hours <br><br>
                    Day 10 <br><br>
                    In the morning transfer to Uzbekistan-Kyrgyzstan border point. Crossing the border. Upon
                    arrival at the customs and passport control on both sides, kindly proceed to the exit of the
                    border control area. At that point, our guide/driver will be waiting for you with the sign with
                    your name. In Kyrgyzstan meet your local guide and driver, transfer to Osh.
                    Have a short city tour in Osh which according to legends was founded by King Solomon
                    3000 years ago.
                    Visit Suleiman-Too (Solomon’s Mountain), National museum of History and Archaeology,
                    and the Osh bazar located at that same place from time when Osh was an important trading
                    center on the Great Silk Road . In the afternoon transfer to local airport for flight to
                    Bishkek. <br>
                    Upon arrival you will be transferred to the hotel. Overnight at the hotel. <br><br>
                    Day 11 <br><br>
                    After breakfast transfer to the Ala-Archa National Park (40 km, 50 min.) which attracts
                    visitors with its beautiful mountain scenery and hiking trails. Fast flowing Ala-Archa River
                    runs through the alpine gorge surrounded by snowy peaks with steep forested slopes.
                    Enjoy free time or a hike in the park. In the afternoon return to Bishkek for city tour. Visit
                    Oak Park, Ala-Too square, Statue of Manas. See Philarmonic Hall and Victory Monument –
                    bright examples of Soviet architecture. Overnight at the hotel. <br>
                    Duration of sightseeing tour: 3-4 hours <br><br>
                    Day 12 <br><br>
                    In the morning transfer to Issyk-Kul Lake (260 km, 4 h.). Stop on the way to see Burana
                    Tower, what has left from the ancient city of Balasagun (11-13 cent.). Continue driving to
                    Issyk-Kul, the second largest mountain lake in the world (1600 m above sea level). Arrival
                    at lake, accommodation in a hotel at the North coast of Issyk-Kul. <br>
                    Meals: breakfast, dinner <br><br>
                    Day 13 <br><br>
                    After breakfast at transfer to Grigoriyevskoe Gorge. It is one of the most beautiful gorges in
                    Issyk-Kul region. After hiking transfer to Bishkek. On the way stop in Cholpon-Ata, where
                    ou will be able to visit Petroglyphs, the rock carvings lying under the open sky and also
                    visit Etnographical museum. Transfer to Bishkek. Arrive in Bishkek, free time. Farewell
                    dinner. Overnight at the hotel. <br>
                    Meals: breakfast, dinner <br><br>
                    Day 14 <br><br>
                    Check-out from the hotel till noon. Transfer to the airport. End of the 14-day
                    UzbekistanKyrgyzstan tour.
                    </p>
