                    <h1>Uzbekistan Tour</h1> <br>
                    Day 1 Tashkent <br> <br>
                    Arrival in Tashkent in the morning with a flight. Meet and greet at the airport,
                    transfer to hotel for rest after the long flight. Free time until lunch to refresh up,
                    lunch at a local restaurant. Afternoon tour in Tashkent including visits to Chorsu
                    handicraft Bazaar, Kukaldosh Madrasah and The Khast Imam Complex. Dinner at a
                    local restaurant and back to hotel for overnight. <br><br>
                    Day 2 Tashkent <br> <br>
                    Samarkand After early breakfast, transfer to the train station at 7 am, take the
                    fast train "Afrosiyob" to Samarkand leaving at 8 am, arriving in Samarkand at
                    10:15am. On the way, you will have the opportunity to view the local cotton
                    fields, villages located along the way. Lunch in local restaurant in Samarkand upon
                    arrival. Accommodation and start the afternoon city tour in Samarkand: visit to
                    the world famous Registan Square with it's breathe-taking three Madrasahs:
                    Ulugbek Madrasah, Sherdor Madrasah and Tillya Kari Madrasah. time to sit, relax
                    and even to read your favorite book by the Registan Square while watching the
                    sunset closer to evening. <br><br>
                    Day 3 Samarkand <br><br>
                    Samarkand, sometimes called the ‘Rome of the Orient’ was already flourishing
                    when Rome and Babylon were founded. During the course of our full day tour we
                    visit the Bibi Khanum Mosque, Mausoleum of Tamerlane, Observatory of
                    Ulughbek, the site where Uleg Bek, grandson of Tamerlane, made some of the
                    greatest achievements of the pre-telescope era of astronomy in the 15th century.
                    We also visit the regal mausoleums of Shah-I Zinda, site of Central Asia’s most
                    sacred Islamic shrine, the massive mosque of Bibi Khanym, and the adjacent
                    colorful market. You can purchase Samarkand’s famous bread which Alexander
                    the Great unsuccessfully tried to have duplicated back home in Greece, or try
                    some of the region’s local produce, from grapes to the world’s tastiest melons of
                    multiple varieties. Our afternoon tour covers the Afrosiab Museum, located on
                    the site of ancient Afrosiab. <br><br>
                    Day 4 Samarkand Bukhara <br><br>
                    After breakfast leave for Bukhara via ancient town of Nurata. There are a lot of
                    legends related with the origin of the city and its name. People refer the origin of
                    the city to the fortress Nur, which was founded by Alexander the Great in 4th
                    century BC. However, according to archaeological excavations the cultural layer in
                    this area reaches the age of 40 thousand years. Supposedly, the main reason to
                    choose this place for settlement was the spring, known as Chashma. Arrive in
                    Nurata. Visit the mausoleum of Nurata and the ruins of the Alexander the Great’s
                    fortress. Continue to Bukhara(200km). On the way to Bukhara stop in Gijduvan. A
                    small town of Gijduvan is situated in 46 km northeast from Bukhara. From the
                    ancient times till the present Gijduvan has been considered as important trading
                    town and crafts centre. Visit the ceramics workshop of Narzullayev’s family
                    where you will be able to become acquainted with the process of producing
                    ceramics; to visit the ceramics museum; to buy manufactured articles of Gijduvan
                    craftsmen; Arrive in Bukhara and accommodation at our hotel. <br><br>
                    Day 5 Bukhara <br><br>
                    Today we explore the old quarter on foot. Bukhara is an ancient city with its
                    bustling “trade domes,” active mosques, and the Kalyan Minaret (“tower of
                    death”), one of Central Asia’s tallest. We’ll stroll around the streets and visit the
                    old town center where old men with long white beards gossip for hours while
                    drinking tea. Bukhara has been designated a World Heritage Site; the resulting
                    restoration of some of the mosques, constructed in the 11th to 15th centuries, to
                    make them appear as if they were built yesterday. There are even some
                    structures still standing that date back to Zoroastrian times 2,500 years ago. We
                    also visit the massive fortress, The Ark, with the Emir’s open-air throne room and
                    the Mausoleum of Ismail Samani, an intricate and beautiful 10th century brick
                    structure that is nicely preserved. We’ll also venture to the outskirts of Bukhara
                    and visit the summer palace of the last emir, built by a Russian Czar with grand
                    halls and a harem. We will also see Char Minar, a gate of a ancient Madrasah long
                    time gone. It was built in 1809 and has more in common with Indian art styles
                    than local. Its name comes from Tajik although the towers you can see are not
                    actually minarets but simply decorative towers. Evening watch the traditional
                    Bukhara folk show. <br><br>
                    Day 6 Bukhara <br><br>
                    After breakfast visit to the outside of the town, to visit the summer residence of
                    the last Khan (ruler) of Bukhara- Sitorai Mokhi Hosa. afternoon free in the old
                    town to relax, explore the narrow streets and get acquainted with the locals.
                    Dinner at Nadir Divanbegi Madrasah with fashion and folklore show introducing
                    the Uzbek classic musical instruments and clothes from different region of the
                    country. <br><br>
                    Day 7 Bukhara <br><br>
                    Khiva After breakfast transfer to Khiva, known as the ancient town frozen in
                    time for it's lovely architecture and individual style. Full day in transfer with stop
                    on the way at local chaykhana for picnic lunch. Opportunity to take pictures of the
                    Amu- Darya river. Upon arrival in Khiva accommodation and free time for resting
                    after the long day of driving. <br><br>
                    Day 8 Khiva <br><br>
                    Full day sightseeing tour in Ichan Kala (inside the walls) part of the town. Legend
                    has it that Khiva was founded when Shem, son of Noah, discovered a well here.
                    The town certainly existed by the 8th Century, as a minor fort and trading post on
                    a Silk Road branch to the Caspian Sea and the Volga. In the early 16th century,
                    Khiva was made capital of the Timurid Empire, becoming a busy slave market and
                    pivot of the khanate for the next three centuries. Until Russia finally wrested the
                    region from Timurid grasp in the 19th century, even the boldest hearts feared
                    encounters with these fierce tribesmen and their desert territory. Khiva is an
                    ancient walled city and looks much like it did in centuries past. It is one of the few
                    sites in Central Asia where one can easily imagine life during the time of the
                    emirates. We will spend the morning wandering its maze of ruins and great
                    mosques. Later, a tour of this living museum including the Ichon-Qala gates
                    Muhammad Amin and Rakhim Khan’s Medressas, Tosh-Khovli Palace, Islom-Huja
                    Medressa and Minaret. Dinner will be served at Toza Bogh Palace (Summer
                    residence of Muhammad Rahim Khan II). Evening flight back to Tashkent. <br><br>
                    Day 9 <br><br>
                    Tashkent After breakfast flight back home. (we advise to take an extra day in
                    Tashkent before flight back home to explore the town on your own, relax and
                    calm down after the exciting yet a bit tiring tour).
                    </p>
